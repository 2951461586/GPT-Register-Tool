def _snapshot_token_files(directory):
    result = {}
    if not directory.exists() or not directory.is_dir():
        return result
    for path in directory.glob("*.json"):
        with contextlib.suppress(Exception):
            stat = path.stat()
            result[str(path.resolve()).lower()] = (stat.st_mtime, stat.st_size)
    return result


def _changed_token_files(directory, before, started):
    if not directory.exists() or not directory.is_dir():
        return []
    changed = []
    for path in sorted(directory.glob("*.json")):
        try:
            stat = path.stat()
            key = str(path.resolve()).lower()
            old = before.get(key)
            if old is None or old != (stat.st_mtime, stat.st_size) or stat.st_mtime >= started - 2:
                changed.append(path)
        except Exception:
            continue
    return changed


def _quote_arg(value):
    text = str(value)
    if re.search(r"\s", text):
        return '"' + text.replace('"', '\\"') + '"'
    return text


def _node_executable():
    return os.environ.get("NODE") or os.environ.get("NODE_EXE") or "node"


def _sanitize_kyl_command(cmd):
    safe = list(cmd)
    for i, value in enumerate(safe[:-1]):
        if str(value) == "--fingerprint":
            raw = str(safe[i + 1])
            safe[i + 1] = raw[:18] + "..." if raw else ""
    return safe


def _terminate_process_tree(proc):
    if os.name == "nt":
        with contextlib.suppress(Exception):
            subprocess.run(["taskkill", "/F", "/T", "/PID", str(proc.pid)], stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL, timeout=10)
            return
    with contextlib.suppress(Exception):
        proc.kill()
